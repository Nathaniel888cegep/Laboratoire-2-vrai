# Demander à l'utilisateur d'entrer un nombre quelconque
nombre_str = input("Entrer un nombre svp : ")

# Convertir le nombre en entier
nombre_entier = int(nombre_str)

# Convertir le nombre en type flottant
nombre_flottant = float(nombre_str)

# Afficher les résultats
print(f"Nombre en entier : {nombre_entier}")
print(f"Nombre en flottant : {nombre_flottant}")